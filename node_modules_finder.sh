#!/bin/bash

desktop_path="$HOME/Desktop"
target_folder="node_modules"

# Find all folders named "node_modules" on the Desktop
found_folders=($(find "$desktop_path" -mindepth 3 -maxdepth 3 -type d -name "$target_folder"))

if [ ${#found_folders[@]} -eq 0 ]; then
    echo "No folders named '$target_folder' found on the Desktop."
    exit 0
fi

# Display unique paths up to the "node_modules" folder
echo "Folders named '$target_folder' found on the Desktop:"
for folder in "${found_folders[@]}"; do
    echo "$folder"
done | sort -u
